"use strict";
// *** string 
let nome1 = 'felipe';
let nome2 = 'felipe';
// foi atribuido o tipo string para a variavel nome pela inicilização
// é exibido um alerta de erro ao atribuir valores de outro tipo, porém consegue converter em javascript e executar no browser
nome1 = 28;
// *** number
console.log(nome1, nome2);
// outra forma declarar uma variável, ja declarando o tipo
// typescript considera todos os tipos numericos como number, seja inteiros ou não
let idade1 = 30;
let idade2 = 30;
console.log(idade1, idade2);
// *** boolean
let possuiHobbies1 = true;
let possuiHobbies2 = true;
console.log(possuiHobbies1, possuiHobbies2);
